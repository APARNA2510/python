def add(a, b):
    d = a+b
    return d

result = add(10, 10)
print("add :", result)    


def sub(a, b):
    f = a-b
    return f

result = sub(30, 10)
print("sub :", result) 


def mul(a, b):
    g = a*b
    return g

result = mul(30, 10)
print("mul :", result) 


def div(a, b):
    j = a/b
    return j

result = div(30, 10)
print("div :", result) 